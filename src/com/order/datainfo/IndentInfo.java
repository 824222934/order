package com.order.datainfo;

public class IndentInfo {
	private String indentid; 
	private String listmenu; 
	private String totalprice; 
	private String indentstate; 
	private String indenttime; 
	private String userid; 
	private String sellerid;
	public String getIndentid() {
		return indentid;
	}
	public void setIndentid(String indentid) {
		this.indentid = indentid;
	}
	public String getListmenu() {
		return listmenu;
	}
	public void setListmenu(String listmenu) {
		this.listmenu = listmenu;
	}
	public String getTotalprice() {
		return totalprice;
	}
	public void setTotalprice(String totalprice) {
		this.totalprice = totalprice;
	}
	public String getIndentstate() {
		return indentstate;
	}
	public void setIndentstate(String indentstate) {
		this.indentstate = indentstate;
	}
	public String getIndenttime() {
		return indenttime;
	}
	public void setIndenttime(String indenttime) {
		this.indenttime = indenttime;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getSellerid() {
		return sellerid;
	}
	public void setSellerid(String sellerid) {
		this.sellerid = sellerid;
	} 
}
