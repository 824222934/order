package com.order.datainfo;

public class SellerInfo {
	private String userid;
	private String shopname;
	private String grade;
	private String shoplocal;
	private String peopleinline;
	private String shoptype;
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getShopname() {
		return shopname;
	}
	public void setShopname(String shopname) {
		this.shopname = shopname;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getShoplocal() {
		return shoplocal;
	}
	public void setShoplocal(String shoplocal) {
		this.shoplocal = shoplocal;
	}
	public String getPeopleinline() {
		return peopleinline;
	}
	public void setPeopleinline(String peopleinline) {
		this.peopleinline = peopleinline;
	}
	public String getShoptype() {
		return shoptype;
	}
	public void setShoptype(String shoptype) {
		this.shoptype = shoptype;
	}
	
}
